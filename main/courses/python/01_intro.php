<h1>Introduction to Python and IDE Setup</h1>
<p>Python is a high-level, interpreted programming language that has taken the world by storm. Known for its "Zen" and clean syntax, it is the language of choice for Data Science, AI, and rapid automation.</p>

<h2>Why Python?</h2>
<ul>
    <li><strong>Readability:</strong> Python code looks like English. It uses indentation to define code blocks instead of curly braces.</li>
    <li><strong>Versatility:</strong> From web apps with Django to machine learning with TensorFlow, Python can do it all.</li>
    <li><strong>Massive Ecosystem:</strong> Over 400,000 packages are available on the Python Package Index (PyPI).</li>
</ul>

<h2>The Development Environment</h2>
<p>To start coding in Python, you need two things: the <strong>Python Interpreter</strong> and a <strong>Code Editor</strong>.</p>

<h3>1. Installing Python</h3>
<p>Download the latest version from <a href="https://python.org" target="_blank">python.org</a>. During installation, <strong>CRITICAL:</strong> make sure to check the box that says <strong>"Add Python to PATH"</strong>.</p>

<h3>2. Choosing an IDE</h3>
<ul>
    <li><strong>VS Code:</strong> The most popular choice. Lightweight and powerful with the Python extension.</li>
    <li><strong>PyCharm:</strong> A professional-grade IDE specifically for Python.</li>
    <li><strong>IDLE:</strong> The simple, built-in editor that comes with Python.</li>
</ul>

<h2>Your First Script</h2>
<p>In Python, you don't need `main` functions or boilerplates to get started. Just one line is enough:</p>

<pre><code class="language-python"># This is a comment
print("Hello, Computer Mind!")</code></pre>

<div class="alert alert-info">
    <strong>Pro Tip:</strong> Check your version by typing <code>python --version</code> in your terminal. Most modern systems use Python 3.
</div>

<h1>Introduction to Control Structures in JavaScript</h1>

<h2>What are Control Structures?</h2>
<p>Control structures are programming constructs that allow you to control the flow of your program's execution. Instead of running code line by line from top to bottom, control structures let you make decisions, repeat code, and create more complex logic.</p>

<h3>Types of Control Structures:</h3>
<ul>
    <li><strong>Conditional statements</strong> - Make decisions (if, else, switch)</li>
    <li><strong>Loops</strong> - Repeat code (for, while, do-while)</li>
    <li><strong>Jump statements</strong> - Alter flow (break, continue, return)</li>
</ul>

<p>In this course, we'll focus on conditional statements and decision-making structures.</p>

<h2>Why Control Structures Matter</h2>
<p>Without control structures, every program would execute the exact same way every time. Control structures enable programs to:</p>
<ul>
    <li>Make decisions based on user input</li>
    <li>Respond differently to different conditions</li>
    <li>Automate repetitive tasks</li>
    <li>Create interactive and dynamic applications</li>
</ul>

